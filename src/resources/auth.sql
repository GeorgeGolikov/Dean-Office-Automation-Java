CREATE OR REPLACE PROCEDURE get_user (user_cursor OUT SYS_REFCURSOR, loginn IN VARCHAR2,
                                    passwordd IN VARCHAR2)
    IS
BEGIN
    open user_cursor for
        SELECT login, password FROM my_users
        WHERE login = loginn AND password = passwordd;
END;