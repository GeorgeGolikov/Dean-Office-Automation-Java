CREATE TABLE my_users (
    id NUMBER GENERATED BY DEFAULT as IDENTITY(START with 1 INCREMENT by 1 NOCACHE),
    login VARCHAR2(50) NOT NULL,
    password VARCHAR2(50) NOT NULL,
        CONSTRAINT my_users_pk PRIMARY KEY (id)
);

